// Operadores de atribuição em Javascript

// Atribuição
let atribuicao = 2;
// Adição e atribuição
atribuicao += 2;
// Subtração e atribuição
atribuicao -= 2;
// Multiplicação e atribuição
atribuicao *= 2;
// Divisão e atribuição
atribuicao /= 2;
// Módulo e atribuição
atribuicao %= 2;

console.log(atribuicao)
