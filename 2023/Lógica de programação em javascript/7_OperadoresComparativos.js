// Operadores de comparação em Javascript

// Igual
let igual = 2 == 2;
// Diferente
let diferente = 2 != 2;
// Estritamente igual
let estritamenteIgual = 2 === 2;
// Estritamente diferente
let estritamenteDiferente = 2 !== 2;
// Maior que
let maiorQue = 2 > 2;
// Maior ou igual que
let maiorOuIgualQue = 2 >= 2;
// Menor que
let menorQue = 2 < 2;
// Menor ou igual que
let menorOuIgualQue = 2 <= 2;

